package modelo;

public class Enseres {
	private String code;
	private Integer codcat;
	private String nombreenser;
	private String descripcion;
	private Integer cantidad;
	private Double precioref;
	private String foto;
	private Double preciorep;
	private Integer estado;
	
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public Integer getCodcat() {
		return codcat;
	}
	public void setCodcat(Integer codcat) {
		this.codcat = codcat;
	}
	public String getNombreenser() {
		return nombreenser;
	}
	public void setNombreenser(String nombreenser) {
		this.nombreenser = nombreenser;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	public Integer getCantidad() {
		return cantidad;
	}
	public void setCantidad(Integer cantidad) {
		this.cantidad = cantidad;
	}
	public Double getPrecioref() {
		return precioref;
	}
	public void setPrecioref(Double precioref) {
		this.precioref = precioref;
	}
	public String getFoto() {
		return foto;
	}
	public void setFoto(String foto) {
		this.foto = foto;
	}
	public Double getPreciorep() {
		return preciorep;
	}
	public void setPreciorep(Double preciorep) {
		this.preciorep = preciorep;
	}
	public Integer getEstado() {
		return estado;
	}
	public void setEstado(Integer estado) {
		this.estado = estado;
	}
		
}
